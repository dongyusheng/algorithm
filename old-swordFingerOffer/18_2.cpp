#include <cstdio>
#include <cstdlib>

using namespace std;

struct ListNode {
    int m_nValue;
    ListNode *m_pNext;
};

/**
 * @description: 删除链表中重复的节点
 * @param: 链表
 * @return {type} 
 * @author: Dongshao
 */
void DeleteDuplication(ListNode **pHead)
{
    if(pHead == nullptr || *pHead == nullptr)
        return;
    
    ListNode *pPreNode = nullptr;
    ListNode* pNode = *pHead;

    while(pNode != nullptr)
    {
        ListNode *pNext = pNode->m_pNext;
        bool needDelete = false;

        // 如果下一个节点的值与当前的值相等, 需要删除
        if(pNext != nullptr && pNext->m_nValue == pNode->m_nValue)
            needDelete = true;
        
        // 不需要删除, 移动指针即可
        if(!needDelete)
        {
            pPreNode = pNode;
            pNode = pNode->m_pNext;
        }
        // 需要删除节点
        else
        {
            int value = pNode->m_nValue;
            ListNode *pToBeDel = pNode;

            // 如果有多个相同值的节点, 删除
            while(pToBeDel != nullptr && pToBeDel->m_nValue == value)
            {
                pNext = pToBeDel->m_pNext;
                delete pToBeDel;
                pToBeDel = nullptr;
                pToBeDel = pNext;
            }

            // 如果pPreNode为空, 说明从链表头到pNext的前一个节点都被删除了, 那么重新将pHead指向pNext即可
            if(pPreNode == nullptr)
                *pHead = pNext;
            // 如果pPreNode指向一个节点, 那重新连接即可
            else
                pPreNode->m_pNext = pNext;
            
            // 将pNode重新赋值
            pNode = pNext;
        }
    }
}


ListNode* CreateListNode(int value)
{
    ListNode* pNode = new ListNode();
    pNode->m_nValue = value;
    pNode->m_pNext = nullptr;

    return pNode;
}

void DestroyList(ListNode* pHead)
{
    ListNode* pNode = pHead;
    while(pNode != nullptr)
    {
        pHead = pHead->m_pNext;
        delete pNode;
        pNode = pHead;
    }
}

void ConnectListNodes(ListNode* pCurrent, ListNode* pNext)
{
    if(pCurrent == nullptr)
    {
        printf("Error to connect two nodes.\n");
        exit(1);
    }

    pCurrent->m_pNext = pNext;
}

void PrintList(ListNode* pHead)
{
    printf("PrintList starts.\n");
    
    ListNode* pNode = pHead;
    while(pNode != nullptr)
    {
        printf("%d\t", pNode->m_nValue);
        pNode = pNode->m_pNext;
    }

    printf("\nPrintList ends.\n");
}

void PrintListNode(ListNode* pNode)
{ 
    if(pNode == nullptr)
    {
        printf("The node is nullptr\n");
    }
    else
    {
        printf("The key in node is %d.\n", pNode->m_nValue);
    }
}

void Test(char* testName, ListNode** pHead, int* expectedValues, int expectedLength)
{
    if(testName != nullptr)
        printf("%s begins: ", testName);

    DeleteDuplication(pHead);

    int index = 0;
    ListNode* pNode = *pHead;
    while(pNode != nullptr && index < expectedLength)
    {
        if(pNode->m_nValue != expectedValues[index])
            break;

        pNode = pNode->m_pNext;
        index++;
    }

    if(pNode == nullptr && index == expectedLength)
        printf("Passed.\n");
    else
        printf("FAILED.\n");
}

